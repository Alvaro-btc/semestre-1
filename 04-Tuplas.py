#CREANDO E INSTANCIANDO UNA TUPLA
estudiantes = ('Samir', 'Matias', 'Hector', ' Pia', 'Carlos')

#IMPRIMIENDO UNA TUPLA
print(estudiantes)

#ELIMINANDO EL ULTIMO ELEMENTO DE LA TUPLA
estudiantes.pop()
print(estudiantes)

#FUNCIÓN INDEX EN TUPLAS (INDICA LA POSICIÓN DEL ELEMENTO)
print(estudiantes.index('Carlos'))

#MÉTODO SORTED EN TUPLAS (ORDENA ELEMENTOS Y LO PASA A LISTA)
print(sorted(estudiantes))















